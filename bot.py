def send_message(chat_id, text):
    url = f"https://api.telegram.org/bot{TOKEN}/sendMessage"
    try:
        requests.post(url, json={"chat_id": chat_id, "text": text, "parse_mode": "Markdown"})
    except Exception as e:
        print(e)

@app.route('/webhook', methods=['POST'])
def webhook():
    data = request.get_json()
    if not data or 'message' not in data:
        return "OK", 200
    
    chat_id = data['message']['chat']['id']
    text = data['message'].get('text', '').lower().strip()
    
    # Команда /start
    if text == '/start':
        user_step[chat_id] = 1
        send_message(chat_id, STEPS[1]["question"])
        return "OK", 200
    
    # Команда /reset
    if text == '/reset':
        user_step[chat_id] = 1
        send_message(chat_id, "🔄 **Квест сброшен!** Давай начнём заново!\n\n" + STEPS[1]["question"])
        return "OK", 200
    
    # Получаем текущий шаг
    step = user_step.get(chat_id, 0)
    
    if step == 0:
        send_message(chat_id, "🍬 Напиши /start, чтобы начать увлекательное приключение с профессором Сладковым!")
        return "OK", 200
    
    # Проверяем ответ
    if text == STEPS[step]["answer"]:
        send_message(chat_id, STEPS[step]["success"])
        next_step = STEPS[step]["next"]
        if next_step == 0:
            user_step[chat_id] = 0
        else:
            user_step[chat_id] = next_step
    else:
        send_message(chat_id, "❌ **Неправильно!** Попробуй ещё раз! Внимательно прочитай подсказку и подумай. Ты сможешь! 💪")
    
    return "OK", 200

